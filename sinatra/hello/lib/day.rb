class Day
 def self.convert(input)
 input = input.to_s
  if input == "Monday"
    "Thingal"
  elsif input == "Tuesday"
    "Shevvai"
  elsif input == "Wednesday"
    "Phuthan"
  elsif input == "Thursday"
    "Viyalan"
  elsif input == "Friday"
    "Velli"
  elsif input == "Saturday"
    "Saeni"
  else input == "Sunday"
    "Gnayeru"
 end
end
end


